public class Income {
    private double in;
    private String name;

    public Income(double in, String name) {
        this.in = in;
        this.name = name;
    }

    public double getIn() {
        return in;
    }

    public void setIn(double in) {
        this.in = in;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}


